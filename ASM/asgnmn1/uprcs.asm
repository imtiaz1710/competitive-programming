.MODEL SMALL
.STACK 100H
.DATA 
	MSG DB 'NAME: IMTIAZ ZAMAN',0DH,0AH,'ID : 17103024',0DH,0AH,'$';
	MSG2 DB 0DH,0AH,'$';
.CODE 
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,MSG
	MOV AH,9
	INT 21H 
	
	MOV AH,1
	INT 21H
	
	LEA DX,MSG2
	MOV AH,9
	INT 21H
	
	CMP AL,'A'
	JNGE ENDCASE
	CMP AL,'Z'
	JNLE ENDCASE
	

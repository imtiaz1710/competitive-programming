.MODEL SMALL
.STACK 100H
.DATA 
	MSG DB 'NAME: IMTIAZ ZAMAN',0DH,0AH,'ID : 17103024',0DH,0AH,'$';
	MSG2 DB 0DH,0AH,'$';
.CODE 
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,MSG
	MOV AH,9
	INT 21H 
	
	MOV AH,1
	INT 21H
	
	LEA DX,MSG2
	MOV AH,9
	INT 21H
	
	CMP AL,'1'
	JE ODD
	
	CMP AL,'3'
	JE ODD
	
	CMP AL,'2'
	JE EVN
	
	CMP AL,'4'
	JE EVN
	
	JMP END_CASE
	
	ODD:
		MOV DL,'o'
		JMP DISPLAY
	EVN:
		MOV DL,'e'
		JMP DISPLAY
	
	
		
	DISPLAY:
		MOV AH,2
		INT 21H
		
		MOV AH,1
		INT 21H
		JMP END_CASE
	END_CASE:
		MOV AH,4CH
		INT 21H
		
MAIN ENDP
END MAIN
	
		
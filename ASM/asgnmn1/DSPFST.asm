.MODEL SMALL
.STACK 100H
.DATA 
	MSG DB 'NAME: IMTIAZ ZAMAN',0DH,0AH,'ID : 17103024',0DH,0AH,'$';
	MSG2 DB 0DH,0AH,'$';
.CODE 
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,MSG
	MOV AH,9
	INT 21H 
	
	MOV AH,1
	INT 21H
	
	MOV BL,AL
	
	MOV AH,2
	MOV DL,' '
	INT 21H
	
	MOV AH,1
	INT 21H
	
	MOV BH,AL 
	
	LEA DX,MSG2
	MOV AH,9
	INT 21H
	
	CMP BL,BH
	JB PRNT
	
	MOV DL,BH
	MOV AH,2
	INT 21H
	JMP ENDCASE
	
	PRNT:
	MOV DL,BL
	MOV AH,2
	INT 21H
	JMP ENDCASE
	
	ENDCASE:
		MOV AH,1
		INT 21H
		MOV AH,4CH
		INT 21H
MAIN ENDP
END MAIN
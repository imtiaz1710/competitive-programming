.MODEL SMALL
.STACK 100H
.DATA 
CNT DB 2
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,1
	INT 21H
	
	MOV BL,AL
	MOV CL,4
	
	LABEL1:
		MOV DL,BL
		SHR DL,CL
		cmp DL,9H
		JG LETTER
		ADD DL,30H
		JMP PRINT
		LETTER:
			ADD DL,37H
		PRINT:
			MOV AH,2
			INT 21H
			ROR BL,CL 
			DEC CNT
			JNZ LABEL1
	MOV AH,4CH
	INT 21H
	MAIN ENDP 
	END MAIN
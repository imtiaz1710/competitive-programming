.MODEL SMALL 
.STACK 100H
.CODE
MAIN PROC
	MOV AH,1
	INT 21H
	XOR BX,BX
	MOV CL,4
	LABEL1:
		CMP AL,0DH
		JZ ENDLABEL
		CMP AL,39H
		JG LETTER
		AND AL,0FH
		JMP SHIFT
		LETTER:
			SUB AL,37H
		SHIFT:
			SHL BL,CL
			OR BL,AL
			
			INT 21H
			JMP LABEL1
	ENDLABEL:
		MOV DL,BL
		MOV AH,2
		INT 21H
		MOV AH,4CH
		INT 21H
MAIN ENDP
END MAIN